class Lesson01 {
    constructor(descriptionImgAdd, descriptionImg02) {
        this.descriptionImgAdd = descriptionImgAdd;
        this.descriptionImg02 = descriptionImg02;
    }
    showDescription(item, linkPage) {
        this.descriptionImgAdd.style.display = "none";
        this.descriptionImg02.style.display = "none";
        switch (item.id) {
            case "addtask": {
                linkPage.href = "AdditionalTask.html";
                this.descriptionImgAdd.style.display = "block";
                break;
            }
            case "task2": {
                linkPage.href = "Task02.html";
                this.descriptionImg02.style.display = "block";
                break;
            }
            case "taskMyReseach": {
                linkPage.href = "MyReseach.html";
                break;
            }
            case "taskMyReseach2": {
                linkPage.href = "TabularLayout.html";
                break;
            }
            default: {
                linkTaskPage.href = "#";
            }
        }
    }
}

